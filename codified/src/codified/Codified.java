package codified;

public class Codified {
	private String nom;
	
	public Codified(String nom) {
		super();
		this.nom = nom;
	}
	
	

	public Codified() {
		super();
	}



	@Override
	public String toString() {
		return "Codified [nom=" + nom + "]";
	}



	public static void main(String[] args) {
		System.out.println("apples");
	}
}
 
